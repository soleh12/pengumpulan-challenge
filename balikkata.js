function soleh(str){
	return str.split('').reverse().join('');
}
console.log(soleh('jakarta'));
console.log(soleh('Hello World and Coders')); // sredoC dna dlroW olleH
console.log(soleh('John Doe')); // eoD nhoJ
console.log(soleh('I am a bookworm')); // mrowkoob a ma I
console.log(soleh('Coding is my hobby')); // ybboh ym si gnidoC
console.log(soleh('Super')); // repuS